#include "libft.h"

void	*ft_memset(void *b, int c, t_size len)
{
	t_size	i;

	i = 0;
	while (i < len)
	{
		((unsigned char *)b)[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
